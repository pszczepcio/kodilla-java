package com.kodilla.stream.Warcaby;

public class ChessFields {
    private final double axisFieldX;
    private final double axisFieldY;

    public ChessFields(double axisFieldX, double axisFieldY){
        this.axisFieldX = axisFieldX;
        this.axisFieldY = axisFieldY;
    }

    public double getAxisFieldX() {
        return axisFieldX;
    }

    public double getAxisFieldY() {
        return axisFieldY;
    }
}
